const seletor = document.getElementById("seletor-louvores");
const botao = document.getElementById("botao-selecionar");
const letra = document.getElementById("letra-musica");
const player = document.getElementById("player");

botao.addEventListener("click", () => {
  const musicaSelecionada = seletor.value;
  letra.textContent = musicas.letra[musicaSelecionada];

  player.src = musicas.audio[musicaSelecionada];
  player.load();
  player.play();
});

// Animações para o seletor de louvores
document.addEventListener('DOMContentLoaded', () => {
  const letraMusica = document.getElementById('letra-musica');
  const player = document.getElementById('player');
  const botao = document.getElementById('botao-selecionar');

  // Animação ao carregar a letra
  function animarLetra() {
    letraMusica.style.opacity = '0';
    letraMusica.style.transform = 'translateY(20px)';

    setTimeout(() => {
      letraMusica.style.opacity = '1';
      letraMusica.style.transform = 'translateY(0)';
    }, 100);
  }

  // Animação do botão
  botao.addEventListener('mouseover', () => {
    botao.style.transform = 'translateY(-2px)';
  });

  botao.addEventListener('mouseout', () => {
    botao.style.transform = 'translateY(0)';
  });

  // Atualizar a animação quando a letra mudar
  const observer = new MutationObserver(animarLetra);
  observer.observe(letraMusica, { childList: true });
});



